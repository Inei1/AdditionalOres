package inei.additionalores.blocks.metalblocks;

import net.minecraft.block.Block;
import net.minecraft.block.material.Material;

public class NetherStarblock extends Block {

	public NetherStarblock(Material material) {
		super(material);
		setHardness(50.0F);
		setBlockName("NetherStarBlock");
		setStepSound(soundTypePiston);
        setHarvestLevel("pickaxe", 9);
        setBlockTextureName("aores:NetherStarBlock");
	}

}
